class Book:
    def __init__(self):
        self.title = None
        self.author = None
        self.pages = None
        self.genre = None

    def set_title(self, title):
        self.title = title

    def set_author(self, author):
        self.author = author

    def set_pages(self, pages):
        self.pages = pages

    def set_genre(self, genre):
        self.genre = genre

